/// Word-level timestamps (in milliseconds) extracted from ElevenLabs audio
/// via Whisper speech recognition. Used for karaoke-style text highlighting.
class WordTiming {
  final String word;
  final int startMs;
  final int endMs;

  const WordTiming(this.word, this.startMs, this.endMs);
}

/// Returns the karaoke word timings for the given [locale].
///
/// Falls back to an empty map for locales without recorded audio yet.
Map<String, List<WordTiming>> voiceTimestampsFor(String locale) {
  switch (locale) {
    case 'en':
      return _voiceTimestampsEn;
    case 'pt':
      return _voiceTimestampsPt;
    default:
      return _voiceTimestampsEn;
  }
}

